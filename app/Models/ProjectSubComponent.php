<?php

namespace App\Models;

use Eloquent as Model;
use Illuminate\Database\Eloquent\SoftDeletes;

/**
 * @SWG\Definition(
 *      definition="ProjectSubComponent",
 *      required={"name,project_component_id"},
 *      @SWG\Property(
 *          property="id",
 *          description="id",
 *          type="integer",
 *          format="int32"
 *      ),
 *      @SWG\Property(
 *          property="project_component_id",
 *          description="project_component_id",
 *           type="integer",
 *          format="int32"
 *      ),
 *      @SWG\Property(
 *          property="name",
 *          description="name",
 *          type="string"
 *      ),
 *      @SWG\Property(
 *          property="description",
 *          description="description",
 *          type="string"
 *      ),
 *      @SWG\Property(
 *          property="created_at",
 *          description="created_at",
 *          type="string",
 *          format="date-time"
 *      ),
 *      @SWG\Property(
 *          property="updated_at",
 *          description="updated_at",
 *          type="string",
 *          format="date-time"
 *      )
 * )
 */
class ProjectSubComponent extends Model
{
    use SoftDeletes;

    public $table = 'project_sub_components';


    protected $dates = ['deleted_at'];



    public $fillable = [
        'name',
        'description',
        'project_component_id'
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'name' => 'string',
        'description' => 'string',
        'project_component_id' => 'string'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static $rules = [
        'name' => 'required',
        'project_component_id' => 'required',
    ];

    public function projectComponent ()
    {
        return $this->belongsTo(ProjectComponent::class, 'project_component_id');
    }

    public function procuring_entities()
    {
        return $this->hasMany(ProcuringEntity::class);
    }


}
